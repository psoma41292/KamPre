/**
 * Normalise a search query: lowercase, trim, collapse spaces.
 * @param {string} q
 * @returns {string}
 */
function normalise(q) {
  return q.toLowerCase().trim().replace(/\s+/g, " ");
}

/**
 * Find products matching a search query.
 * Returns only inStock=true results for the first matching keyword group.
 * Products with inStock=false are silently excluded from comparison.
 *
 * @param {string} query
 * @returns {Array}
 */
function findProducts(query) {
  const q = normalise(query);
  const group = products.find((g) =>
    g.keywords.some((kw) => q.includes(kw) || kw.includes(q))
  );
  if (!group) return [];
  // Filter out unavailable products — do not compare or display them
  return group.results.filter((r) => r.inStock === true);
}

/**
 * Return all distinct keyword groups (for autocomplete suggestions).
 * @returns {string[]}
 */
function getAllKeywords() {
  return products.flatMap((g) => g.keywords);
}

/**
 * Return all product groups with their keywords — used by the /api/groceries endpoint.
 * @returns {Array<{ keywords: string[], category: string }>}
 */
function getAllProducts() {
  return products.map((g) => ({
    keywords: g.keywords,
    category: g.results[0]?.category || "general",
    platforms: g.results.map((r) => r.platform),
    primaryName: g.keywords[0],
  }));
}

module.exports = { findProducts, getAllKeywords, getAllProducts };
